package ai.remi.cms.domain.converter;


import ai.remi.cms.domain.dto.post.ContentCommentPostDTO;
import ai.remi.cms.domain.dto.put.ContentCommentPutDTO;
import ai.remi.cms.domain.entity.ContentComment;
import ai.remi.cms.domain.query.ContentCommentQuery;
import ai.remi.cms.domain.vo.ContentCommentVO;
import org.mapstruct.Mapper;
import org.mapstruct.Mapping;
import org.mapstruct.factory.Mappers;

/**
 * @author DianJiu
 * @email dianjiuxyz@gmail.com
 * @desc 评论记录 ContentCommentConverter转换接口
 */
@Mapper
public interface ContentCommentConverter {

    ContentCommentConverter INSTANT = Mappers.getMapper(ContentCommentConverter.class);

    @Mapping(target = "id", ignore = true)
    @Mapping(target = "deleted", ignore = true)
    @Mapping(target = "revision", ignore = true)
    @Mapping(target = "deptTenantId", ignore = true)
    @Mapping(target = "companyTenantId", ignore = true)
    @Mapping(target = "createdBy", ignore = true)
    @Mapping(target = "createdAt", ignore = true)
    @Mapping(target = "updatedBy", ignore = true)
    @Mapping(target = "updatedAt", ignore = true)
    ContentComment postDtoToEntity(ContentCommentPostDTO contentCommentDTO);

    @Mapping(target = "deleted", ignore = true)
    @Mapping(target = "revision", ignore = true)
    @Mapping(target = "deptTenantId", ignore = true)
    @Mapping(target = "companyTenantId", ignore = true)
    @Mapping(target = "createdBy", ignore = true)
    @Mapping(target = "createdAt", ignore = true)
    @Mapping(target = "updatedBy", ignore = true)
    @Mapping(target = "updatedAt", ignore = true)
    ContentComment putDtoToEntity(ContentCommentPutDTO contentCommentDTO);

    @Mapping(target = "deleted", ignore = true)
    @Mapping(target = "revision", ignore = true)
    @Mapping(target = "deptTenantId", ignore = true)
    @Mapping(target = "companyTenantId", ignore = true)
    @Mapping(target = "createdBy", ignore = true)
    @Mapping(target = "createdAt", ignore = true)
    @Mapping(target = "updatedBy", ignore = true)
    @Mapping(target = "updatedAt", ignore = true)
    ContentComment queryToEntity(ContentCommentQuery contentCommentQuery);

    ContentCommentVO entityToVO(ContentComment contentComment);

}
