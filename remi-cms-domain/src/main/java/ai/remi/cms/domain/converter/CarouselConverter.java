package ai.remi.cms.domain.converter;


import ai.remi.cms.domain.dto.post.CarouselPostDTO;
import ai.remi.cms.domain.dto.put.CarouselPutDTO;
import ai.remi.cms.domain.entity.Carousel;
import ai.remi.cms.domain.query.CarouselQuery;
import ai.remi.cms.domain.vo.CarouselVO;
import org.mapstruct.Mapper;
import org.mapstruct.Mapping;
import org.mapstruct.factory.Mappers;

/**
 * @author DianJiu
 * @email dianjiuxyz@gmail.com
 * @desc 轮播管理 CarouselConverter转换接口
 */
@Mapper
public interface CarouselConverter {

    CarouselConverter INSTANT = Mappers.getMapper(CarouselConverter.class);

    @Mapping(target = "id", ignore = true)
    @Mapping(target = "deleted", ignore = true)
    @Mapping(target = "revision", ignore = true)
    @Mapping(target = "deptTenantId", ignore = true)
    @Mapping(target = "companyTenantId", ignore = true)
    @Mapping(target = "createdBy", ignore = true)
    @Mapping(target = "createdAt", ignore = true)
    @Mapping(target = "updatedBy", ignore = true)
    @Mapping(target = "updatedAt", ignore = true)
    Carousel postDtoToEntity(CarouselPostDTO carouselDTO);

    @Mapping(target = "deleted", ignore = true)
    @Mapping(target = "revision", ignore = true)
    @Mapping(target = "deptTenantId", ignore = true)
    @Mapping(target = "companyTenantId", ignore = true)
    @Mapping(target = "createdBy", ignore = true)
    @Mapping(target = "createdAt", ignore = true)
    @Mapping(target = "updatedBy", ignore = true)
    @Mapping(target = "updatedAt", ignore = true)
    Carousel putDtoToEntity(CarouselPutDTO carouselDTO);

    @Mapping(target = "deleted", ignore = true)
    @Mapping(target = "revision", ignore = true)
    @Mapping(target = "deptTenantId", ignore = true)
    @Mapping(target = "companyTenantId", ignore = true)
    @Mapping(target = "createdBy", ignore = true)
    @Mapping(target = "createdAt", ignore = true)
    @Mapping(target = "updatedBy", ignore = true)
    @Mapping(target = "updatedAt", ignore = true)
    Carousel queryToEntity(CarouselQuery carouselQuery);

    CarouselVO entityToVO(Carousel carousel);

}
