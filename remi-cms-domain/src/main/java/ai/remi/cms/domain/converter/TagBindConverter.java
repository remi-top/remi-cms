package ai.remi.cms.domain.converter;

import ai.remi.cms.domain.dto.post.TagBindPostDTO;
import ai.remi.cms.domain.dto.put.TagBindPutDTO;
import ai.remi.cms.domain.entity.TagBind;
import ai.remi.cms.domain.query.TagBindQuery;
import ai.remi.cms.domain.vo.TagBindVO;
import org.mapstruct.Mapper;
import org.mapstruct.Mapping;
import org.mapstruct.factory.Mappers;

/**
 * @author DianJiu
 * @email dianjiuxyz@gmail.com
 * @desc 标签绑定 TagBindConverter转换接口
 */
@Mapper
public interface TagBindConverter {

    TagBindConverter INSTANT = Mappers.getMapper(TagBindConverter.class);

    @Mapping(target = "id", ignore = true)
    @Mapping(target = "deleted", ignore = true)
    @Mapping(target = "revision", ignore = true)
    @Mapping(target = "deptTenantId", ignore = true)
    @Mapping(target = "companyTenantId", ignore = true)
    @Mapping(target = "createdBy", ignore = true)
    @Mapping(target = "createdAt", ignore = true)
    @Mapping(target = "updatedBy", ignore = true)
    @Mapping(target = "updatedAt", ignore = true)
    TagBind postDtoToEntity(TagBindPostDTO tagBindDTO);

    @Mapping(target = "deleted", ignore = true)
    @Mapping(target = "revision", ignore = true)
    @Mapping(target = "deptTenantId", ignore = true)
    @Mapping(target = "companyTenantId", ignore = true)
    @Mapping(target = "createdBy", ignore = true)
    @Mapping(target = "createdAt", ignore = true)
    @Mapping(target = "updatedBy", ignore = true)
    @Mapping(target = "updatedAt", ignore = true)
    TagBind putDtoToEntity(TagBindPutDTO tagBindDTO);

    @Mapping(target = "deleted", ignore = true)
    @Mapping(target = "revision", ignore = true)
    @Mapping(target = "deptTenantId", ignore = true)
    @Mapping(target = "companyTenantId", ignore = true)
    @Mapping(target = "createdBy", ignore = true)
    @Mapping(target = "createdAt", ignore = true)
    @Mapping(target = "updatedBy", ignore = true)
    @Mapping(target = "updatedAt", ignore = true)
    TagBind queryToEntity(TagBindQuery tagBindQuery);

    TagBindVO entityToVO(TagBind tagBind);

}
