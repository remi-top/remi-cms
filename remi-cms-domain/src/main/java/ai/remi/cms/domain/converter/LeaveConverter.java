package ai.remi.cms.domain.converter;

import ai.remi.cms.domain.dto.post.LeavePostDTO;
import ai.remi.cms.domain.dto.put.LeavePutDTO;
import ai.remi.cms.domain.entity.Leave;
import ai.remi.cms.domain.query.LeaveQuery;
import ai.remi.cms.domain.vo.LeaveVO;
import org.mapstruct.Mapper;
import org.mapstruct.Mapping;
import org.mapstruct.factory.Mappers;

/**
 * @author DianJiu
 * @email dianjiuxyz@gmail.com
 * @desc 评论记录 LeaveConverter转换接口
 */
@Mapper
public interface LeaveConverter {

    LeaveConverter INSTANT = Mappers.getMapper(LeaveConverter.class);

    @Mapping(target = "id", ignore = true)
    @Mapping(target = "deleted", ignore = true)
    @Mapping(target = "revision", ignore = true)
    @Mapping(target = "deptTenantId", ignore = true)
    @Mapping(target = "companyTenantId", ignore = true)
    @Mapping(target = "createdBy", ignore = true)
    @Mapping(target = "createdAt", ignore = true)
    @Mapping(target = "updatedBy", ignore = true)
    @Mapping(target = "updatedAt", ignore = true)
    Leave postDtoToEntity(LeavePostDTO leaveDTO);

    @Mapping(target = "deleted", ignore = true)
    @Mapping(target = "revision", ignore = true)
    @Mapping(target = "deptTenantId", ignore = true)
    @Mapping(target = "companyTenantId", ignore = true)
    @Mapping(target = "createdBy", ignore = true)
    @Mapping(target = "createdAt", ignore = true)
    @Mapping(target = "updatedBy", ignore = true)
    @Mapping(target = "updatedAt", ignore = true)
    Leave putDtoToEntity(LeavePutDTO leaveDTO);

    @Mapping(target = "deleted", ignore = true)
    @Mapping(target = "revision", ignore = true)
    @Mapping(target = "deptTenantId", ignore = true)
    @Mapping(target = "companyTenantId", ignore = true)
    @Mapping(target = "createdBy", ignore = true)
    @Mapping(target = "createdAt", ignore = true)
    @Mapping(target = "updatedBy", ignore = true)
    @Mapping(target = "updatedAt", ignore = true)
    Leave queryToEntity(LeaveQuery leaveQuery);

    LeaveVO entityToVO(Leave leave);

}
